<h4>Sejarah Ompu Tuan Situmorang Sipitu Ama</h4>

<p><b>Ompu Tuan Situmorang</b> adalah anak dan <b>Ompunta Siraja Lontung</b> dan Ibunya <b>Siboru Pareme. Siraja Lontung</b> anak dan <b>Tuan Saribu Raja</b> dan Ibunya <b>Siboru Pareme. Ompu Tuan Situmorang</b> adalah Cucu dan <b>Tuan Saribu Raja.</b>  </p>
<p><b>Ompu Tuan Situmorang</b> salah seorang dan sembilan bersaudara (Tujuh laki-laki dua perempuan) anak dan Siraja Lontung dan Ibunya Siboru Pareme. Adapun anak dan Siraja Lontung / Siboru Pareme adalah sebagai berikut:</p>

<p>1. Ompu Tuan Situmorang,</p>
<p>2. Toga Sinaga,</p>
<p>3. Toga Pandiangan,</p>
<p>4. Toga Nainggolan,</p>
<p>5. Toga Simatupang,</p>
<p>6. Toga Aritonang,</p>
<p>7. Toga Siregar dan Boruna,</p>
<p>8. Siboru amak Pandan Kawin dengan Toga Simamora,</p>
<p>9. Siboru Panggabean kawin dengan Toga Sihombing</p>

<p><b>Ompu Tuan Situmorang lahir di Sabulan Kabupaten Samosir.</b></p>
<p>Ompu Tuan Situmorang yang populer menurunkan marga Situmorang. Ompu Tuan Situmorang mempunyai dua putra dan hasil perkawinannya (pernikahannya) dengan <b>boru Limbong </b>yaitu:</p>
<p><b>1. Ompu Panopa Raja</p>
<p>2. Ompu Pangaribuan</b></p>

<br>
<b><p>Ompu Tuan Situmorang mempunyai pahompu tiga orang yaitu:</p></b><br>
<b><p>1. Anak Ompu Panopa Raja 2 (dua) orang yaitu:</b><br>
a. Ompu Ambolas<br>
b. Parhujobung<br></p>
<br>


<p>
  <b>2. Ompu Pangaribuan mempunyai anak 1 (satu) orang yaitu:</b><br>
  Raja Babiat<br>
  Ompu Tuan Situmorang mempunyai cicit (nini) atau disebut anak mangulahi sebanyak tujuh orang dan cucunya yaitu:<br>
  <br>
  <b>a. Anak Ompu Ambolas 2 (dua) orang yaitu:</b><br>
  1. Raja Pande (Lumban Pande)<br>
  2. Raja Nahor (Lumban Nahor)<br>
  <br>
</p>


<p>
  <b>b. Anak Parhujobung 2 (dua) orang yaitu:</b><br>
  1. Tuan Suhut (Suhut Ni Huta)<br>
  2. Tuan Ringo<br>
  <br>
  <b>c. Anak Raja Babiat 3 (tiga) orang yaitu:</b><br>
  1. Sitohang Uruk (Dori Mangambat)<br>
  2. Sitohang Tonga-tonga (Raja Itubungna)<br>
  3. Sitohang Toruan (Op. Bona Ni Onan)<br>
  <br>
</p>

<p>
  <b>Ompu Tuan Situmorang</b> adalah seorang yang bijaksana, cerdas, pintar, pemberani dan berpikiran jauh kedepan serta mempunyai rasa kasih sayang kepada keluarganya dan kepada Pomparannya. Ompu Tuan Situmorang hidup sangat lanjut (panjang umurnya). Sedangkan anaknya Ompu Panopa Raja, Ompu Pangaribuan beserta tiga orang cucunya yaitu Ompu Ambolas, Parhujobung, Raja Babiat telah meninggal terlebih dahulu jadi dia tinggal bersama-sama dengan cicitnya (nininya) yang tujuh orang yaitu Raja Pande, Raja Nahor, Tuan Suhut Ni Huta, Tuan Ringo, Sitohang Uruk (Dori Nangambat), Sitohang Tonga-tonga (Raja Itubungna), Sitohang Toruan (Op. Bona Ni Onan).<br>
  <br>

  <b>Op. Tuan Situmorang berkeinginan agar pomparannya tetap bersatu dan saling bantu membantu disetiap kegiatan diantara pomparannya (turunannya).</b><br>
<br>
Mengingat bahwa Ompu Tuan Situmorang sudah sangat lanjut usianya, penglihatannya sudah rabun, badannya sudah mulai lemah tetapi semangatnya tetap tinggi untuk membina, menasehati semua pomparannya maka Ompu Tuan Situmorang beserta cicitnya (nininya) sepakat untuk mengadakan upacara pesta ucapan syukur kepada Yang Maha Kuasa atas umur panjang Ompu Tuan Situmorang serta meminta kepada Yang Maha Kuasa agar seluruh cicitnya diberkati dan sekaligus menyampaikan amanah, pesan (Tona) kepada ketujuh cicit (nini) atau anak mangulahinya.<br>
<br>
Upacara pesta pun dilaksanakan sesuai yang direncanakan. Seluruh keluarga besar Ompu Tuan Situmorang diundang dan berkumpul. Menyampaikan ucapan syukur kepada Tuhan Yang Maha Kuasa atas berkat, panjang umur yang diberikan kepada Ompu Tuan Situmorang. Kemudian pada acara tersebut Ompu Tuan Situmorang memanggil ketujuh cicitnya (nininya) atau anak Mangulahinya untuk diberi pasu pasu (berkat), arnanah, pesan (Tona), nasehat dan menyampaikan hal-hal sebagai berikut:<br>
<br>
Mulai hari ini kamu ketujuh cicit (nini) saya tetapkan menjadi anak saya, Sayalah orang tuamu. Semua kalian anak saya (cicit) yang tujuh orang harus tetap bersatu dan bahu membahu di dalam melaksanakan sesuatu yang diperlukan.&nbsp;<b><i>Sisada anak mahamu sisada Boru, Sisada Lulu anak, sisada Lulu di Boru.</i></b> <b><i>Manat Mardongan Tubu, Elek Marboru, Somba Marhula – hula</i></b><br>
<br>
Pada waktu upacara pesta yang dilaksanakan Ompu Tuan Situmorang beserta pomparannya, diadakan penyampaian persembahan (<i>satti-satti</i>) yang disampaikan oleh ketujuh cicitnya (nininya) atau anak mangulahinya kepada Ompu Tuan Situmorang. Dan persembahan inii dilaksanakan secara berurutan sebagai berikut:<br>
<br>
<b>1. Persembahan (<i>Satti-satti</i>) dari Raja Pande (Lumban Pande). </b><br>
Ompu Tuan Situmorang menerimanya dengan mengatakan Maulate (terima kasih), namalo doho, Pande doho, mangula siulaonmu, kamu kerjakanlah apa yang dapat kamu kerjakan untuk yang baik. Jadi goarmu mulai sadarion <b>Jounma ho Raja Pande (Lumban Pande)</b>.<br>
<br>
<b>2.&nbsp;</b><b>Persembahan (<i>Satti-satti</i>) dari</b><b>&nbsp;Raja Nohor (Lumban Nahor). </b><br>
Setelah diterima Ompu Tuan Situmorang mengucapkan terima kasih dan mengatakan banyak kerjamu dapat kamu selesaikan dengan baik, mau bekerja sama, pandai dan selalu kamu menganggap bahwa kerja itu dapat kamu laksanakan jadi mulai hari ini saya panggilkan namamu menjadi <b>Raja Nahor (Lumban Nahor)</b>.<br>
<br>
<b>3.&nbsp;</b><b>Persembahan (<i>Satti-satti</i>) dari</b><b>&nbsp;Tuan Suhut (Tuan Suhut Ni Huta). </b><br>
Setelah diterima persembahan (<i>Satti-satti</i>), Ompu Tuan Situmorang menyampaikan ucapan terima kasih dan mengatakan kamu adalah orang yang baik bijaksana pandai, selalu mau menjadi tumpuan, mempersatukan abang adikmu dan selalu mau menjadi pendorong untuk kebaikan diantara kalian yang abang beradik maka namamu saya panggilkan Tuan Suhut Ni Huta<br>
<br>
<b>4.&nbsp;</b><b>Persembahan (<i>Satti-satti</i>) dari</b><b>&nbsp;Tuan Ringo</b>. <br>
Setelah di terima persembahan (<i>satti-satti</i>), Ompu Tuan Situmorang mengucapkan terima kasih, mengatakan : Kamu adalah orang yang baik dan yang banyak bicara, kamu selalu banyak menyampaikan hal - hal yang perlu diketahui orang lain. Mulai hari ini saya panggilkan kamu menjadi bemama <b>Tuan Ringo</b>.<br>
<br>
<b>5.&nbsp;</b><b>Persembahan (<i>Satti-satti</i>) dari</b><b>&nbsp;Sitohang Uruk (Dori Mangambat). </b><br>
Setelah dIterima persembahan (<i>satti-satti</i>), Ompu Tuan Situmorang mengucapkan terima kasih, diletakkan <i>satti-satti</i> tersebut di atas Tohang yang ada didalam rumah bagian uruk (julu), dan mengatakan Kamu adalah orang yang baik, mau bekerjasama dan mau menghargai abang adikmu. Jadi mulai hari ini saya panggilkan namamu <b>Sitohang Uruk</b>.<br>
<br>
<b>6.&nbsp;</b><b>Persembahan (<i>Satti-satti</i>) dari</b><b>&nbsp;Sitohang Tonga - tonga (Raja Itubungna)</b>. <br>
Setelah diterima persembahan (<i>satti - satti</i>), Ompu Tuan Situmorang mengucapkan terima kasih, diletakkan <i>satti-satti</i> tersebut di atas Tohang yang ada didalam rumah bagian tengah dan mengatakan: Kamu adalah orang yang baik, mau bekerjasama, rajin, dan mau hormat menghormati. Maka saya panggilkan namamu <b>Sitohang Tonga-tonga</b>.<br>
<br>
<b>7.&nbsp;</b><b>Persembahan (<i>Satti-satti</i>) dari</b><b>&nbsp;Sitohang Toruan (Ompu Bona Ni Onan)</b>. <br>
Setelah diterima persembahan (<i>satti - satti</i>), Ompu Tuan Situmorang mengucapkan terima kasih, diletakkan <i>satti-satti</i> tersebut di atas Tohang yang ada didalam rumah bagian hilir (Toruan atau Jae) dan mengatakan: Kamu adalah orang yang baik, bekerja keras, mau bekerja sama, rajin, dan mau hormat menghormati serta di senangi. Maka saya panggilkan namamu <b>Sitohang Toruan</b>.<br>
<br>
</p>

