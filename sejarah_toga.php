  <p><strong>RAJA SILAHISABUNGAN</strong></p>
  <p>Berikut marga-marga keturunan Raja Silahisabunan dengan 2 istrinya.</p>
  <p>Secara berurutan ( dari paling tua ke yang paling muda ) :</p>
  <p>(1) Lohoraja , menurunkan marga : Sihaloho</p>
  <p>(2) Tungkirraja , menurunkan marga : Situngkir, Sipangkar, Sipayung.</p>
  <p>(3) Sondiraja, menurunkan marga : Rumasondi, Rumasingap, Silalahi Parmahan, Naiborhu, Nadapdap, Doloksaribu, Sinurat.</p>
  <p>(4) Butarraja, menurunkan marga : Sidabutar</p>
  <p>(5) Dabaribaraja, menurunkan marga : Sidabariba</p>
  <p>(6) Debangraja , menurunkan marga : Sidebang</p>
  <p>(7) Baturaja , menurunkan marga : Pintubatu, Sigiro</p>
  <p>(8) Si Raja Tambun, menurunkan marga : Tambun, Tambunan, Daulay.</p>

  <p>Pasca dekade Poda Sagu-sagu Marlangan, maka keturunan Silahisabungan akhirnya tergolong dalam marga-marga masing-masing. Satu hal yang berbeda dengan umumnya komunitas di Toba, marga-marga satu keturunan telah saling mengawini. Hal ini dapat difahami mengingat geografis Toba sebelumnya adalah daerah pegunungan dan terisolasi. Contohnya keturunan Lontung,Borbor, Naipospos dan lainnya, mereka meskipun satu leluhur namun telah saling kawin-mengawini. Itu sebabnya kelompok klan Toba umumnya menamakan ikatan kelompok keturunan dengan istilah Toga. Hal berbeda dengan keturunan Raja Silahisabungan yang tidak akan pernah mengenal istilah “toga” karena sesama mereka adalah satu darah, darah Raja Silahisabungan yag tidak boleh saling kawin-mengawini.</p>
  <p><strong>TENTANG MARGA SILALAHI</strong></p>
  <p>Kecuali keturunan Tambunraja alias Si Raja Tambun, kini banyak keturunan Raja Silahisabungan dari Silalahi Nabolak banyak marga Silalahi, umumnya mereka yang sudah merantau meninggalkan negeri Silalahi Nabolak ke negeri sekitarnya, seperti : Sidikalang sekitarnya, Silmakuta dan sekitrnya, Raya, Pamatang Siantar, Serdang, Deli, dsb. Sehingga pemakaian marga Silalahi identik&nbsp; dengan marga kesatuan yang umumnya banyak dipakai semua keturunan Raja Silahisabungan diluar Silalahi Nabolak bahkan di Silalahi Nabolak sendiri. Dan ini adalah kenyataan, seperti di Toba Balige, marga Silalahi dipakai oleh Raja Parmahan alias Raja Bunga-bunga, keturunan Rumasondi. Di Samosir, marga Silalahi dipakai keturunan Debangraja di Pangururan dan Tolping dan keturunan Datu Naboratan keturunan Raja Parmahan Silalahi dari Balige. Di Simalungun, Pakpak, Karo, marga Silalahi dipakai oleh keturunan Sihaloho, Situngkir, Sidabutar, Sidabaraiba, Sidebang, Pintubatu atau Sigiro. Namun yang pasti, ketika dalam pergaulan sesama keturunan Silahisabungan, masing masing mereka memahami dan mengetahui keberadaaan dan posisinya satu sama lain. Di perantauan kini, keturunanan Raja Silahisabungan tetap membuat satu ikatan kekeluargaan yang umumnya disebut Punguan Pomparan Raja Silahisabungan, Boru, Bere Dohot Ibebere.</p>
  <p><strong>ASAL-USUL SILALAHI NABOLAK</strong></p>
  <p>Awalnya Raja Silahisabungan membuka kampung dengan nama Huta Lahi, sesuai namanya, di pesisir Pakpak Dairi. Pasca dekade Poda Sagu-sagu Marlangan, anak-anak Raja Silahisabungan kemudian menikah dan berketurunan. Mereka Kemudian membukan perkampungan menurut kelompok mereka masing-masing. Alhasil, Huta lahi kemudian menjadi meluas (bolak) dan akhirnya keturunan Raja Silahisabungan manamai tanah mereka menjadi Silalahi Nabolak. Dan masa pendudukan kolonial Belanda, secara Administratif Dairi masuk dalam residen Tapanuli, nama Silalahi kemudian dikukuhkan secara administratif dan kemudian tao (danau) disekitarnya tanah Silalahi dinamai Tao Silalahi.</p>
  <p>Negeri Silalahi Nabolak kemudian dibagi menurut 7 anak-anak Silahisabungan dan menamai masing-masing negeri (lumban) menurut nama-nama ketujuh anak-anaknya pula. Namun kini, Negeri Silalahi Nabolak telah terbagi 8 sesuai dengan jumlah anak-anak Raja Silahisabungan.</p>



