<p> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Bagi masyarakat Bangso Batak dan para anthropolog/etnolog telah banyak mengkaji Bagi masyarakat Bangso Batak dan para anthropolog/etnolog telah banyak mengkaji keberadaan marga-marga keturunan Raja Nai Ambaton yang teguh memegang amanat leluhurnya dalam membangun ikatan persaudaraan pada berbagai wilayah di Indonesia sampai ke luar negeri (desa na ualu). Warga Parna dalam berkomunikasi persaudaraan tidak memandang adanya sekat/batas, wilayah penyebaran sub etnis (puak), agama, sosial budaya, sosial ekonomi dan sosial politik. Kenyataan, sebegitu tahu dirinya bagian dari marga PARNA komunikasi akan terbangun secara spontanitas. Ini sudah menjadi kebiasaan dan berlangsung cukup lama, bukan satu abad saja. Telah teruji dalam sejarah perjuangan, zaman revolusi, termasuk dalam menegakkan kemerdekaan RI, demikian dituturkan para orang tua-tua pelaku perjuangan dari berbagai wilayah. Begitu sakral ikatan kekerabatan (pertuturan) PARNA ini bagi individu yang sudah merasakannya. </p>

